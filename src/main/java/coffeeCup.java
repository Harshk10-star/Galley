/**
 * This class is responsible for defining a coffee cup object
 * @author Harsh Kuddu
 */
public class coffeeCup {
    private String material="glass";
    private String color="white";
    int capacity=262;

    /**
     * Constructor
     * @author Harsh Kuddu
     */
    public coffeeCup(){

    }


    /**
     * Getting the material
     * @return the material
     * @author Harsh Kuddu
     */
    public String getMaterial() {
        return material;
    }
    /**
     * Getting the color
     * @return the color
     * @author Harsh Kuddu
     */
    public String getColor() {
        return color;
    }
    /**
     * Getting the capacity
     * @return the capacity
     * @author Harsh Kuddu
     */
    public int getCapacity() {
        return capacity;
    }
}
