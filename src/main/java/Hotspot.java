/**
 * This class is responsible for defining a hotspot object
 * @author Harsh Kuddu
 */
public class Hotspot {
    String material="metal";
    String color="silver";
    int capacity=1575;
    /**
     * Constructor
     * @author Harsh Kuddu
     */
    public Hotspot(){

    }

    /**
     * Getting the material
     * @return the material
     * @author Harsh Kuddu
     */
    public String getMaterial() {
        return material;
    }
    /**
     * Getting the color
     * @return the color
     * @author Harsh Kuddu
     */
    public String getColor() {
        return color;
    }
    /**
     * Getting the capacity
     * @return the capacity
     * @author Harsh Kuddu
     */
    public int getCapacity() {
        return capacity;
    }






}
